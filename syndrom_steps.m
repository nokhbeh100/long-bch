%=========================================================================
%                       This function calculates
%                       syndroms of the received symbols
%=========================================================================
%#########################################################################
function s=syndrom_steps(Rx,t,m)
Rx=(Rx==0)*(2^m-1);% + (Rx==1) * 0
Rx=reshape(Rx,[],1);

H=(0:numel(Rx)-1)';
s=zeros(1,2*t);
for i = 1:2*t
    s(i)=SumDim1( Prod(mod(H*i,2^m-1), Rx, m) ,m);
end
    
%#########################################################################
